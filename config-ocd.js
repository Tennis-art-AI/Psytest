/* =====================================================================
   6.2: ДВУХЧАСТНЫЕ ВОПРОСЫ И ЧЕКЛИСТЫ
   =====================================================================

   DUAL_Q: вопросы с форматом (a) интенсивность 0–5 + (b) частота 0–4.
   Inline JS в HTML создаёт DUAL{} и обработчики .ans-dual.
   При выборе обеих частей: S[q] = dualScore(a, b).
   До этого S[q] = undefined → tot()/dS() пропускают.

   CHK_Q: чеклисты multiple-select. S[q] всегда undefined.
   Inline JS создаёт CHK{} и обработчики .ans-multi.
   Данные в CHK[q] → используются в buildC() для sub-module.

   FUNC_Q: вопросы функционирования (К2). Включены в ans() через FQ,
   но НЕ в tot() (SCORED=23, а FUNC_Q = Q24–Q26).

   Проверка MAX:
   A: Q1(5)+Q2(4)+Q3(4)+Q4(4)+Q5(4)+Q6(5)+Q7(0)+Q8(4)+Q9(4) = 34 ✓
   B: Q10(5)+Q11(0)+Q12(4)+Q13(4)+Q14(4)+Q15(4)+Q16(5)+Q17(4) = 30 ✓
   C: Q18(4)+Q19(4)+Q20(4)+Q21(4)+Q22(4)+Q23(4) = 24 ✓
   Total: 34 + 30 + 24 = 88 ✓                                       */

const DUAL_Q = [1, 6, 10, 16];
