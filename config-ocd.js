      h += '<strong style="color:var(--warm-dk);">Функционирование нарушено при невысоком балле</strong> ';
      h += 'Скрининговый балл невысокий, но вы отметили заметные нарушения в работе, отношениях или быте. ';
      h += 'Обратитесь к специалисту — возможно, симптомы влияют на жизнь сильнее, чем показывает балл.</div>';
