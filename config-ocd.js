/* dualScore: единая функция расчёта балла двухчастного вопроса.
   Вызывается из inline JS: S[q] = dualScore(DUAL[q].a, DUAL[q].b)
   max = Math.round((5 + 4) / 2) = 5 */
function dualScore(a, b) {
  return Math.round((a + b) / 2);
}


