/* config-anxiety.js — конфигурация для диагностики ГТР */

const TQ = 18, FQ = 3, MAX = 72, SCORED = 18;
const DOM = { A:[1,5], B:[6,10], C:[11,14], D:[15,18], E:[19,21] };
const DM = { A:20, B:20, C:16, D:16, E:12 };
const LEV = [
  { min:0,  max:14, l:'НОРМА',          c:'#166534', b:'#86efac' },
  { min:15, max:31, l:'ЛЁГКАЯ',         c:'#6a7a2a', b:'#c8e070' },
  { min:32, max:48, l:'УМЕРЕННАЯ',       c:'#b45309', b:'#fbbf24' },
  { min:49, max:62, l:'ТЯЖЁЛАЯ',        c:'#c2410c', b:'#f97316' },
  { min:63, max:72, l:'КРАЙНЕ ТЯЖЁЛАЯ', c:'#b03a3a', b:'#ef4444' }
];
const DESC = [
  'Скрининг не выявил выраженных признаков',
  'Есть признаки, заслуживающие внимания',
  'Признаки выражены — рекомендуется обращение к специалисту',
  'Выраженные признаки. Обратитесь к специалисту — помощь эффективна',
  'Срочно обратитесь к специалисту. Помощь возможна даже в тяжёлых случаях'
];

function buildC() {
  const t = tot(), a = ans(), hk = Object.keys(HIST).length;
  if (a < 5 && hk === 0) { document.getElementById('conc').style.display = 'none'; return; }
  document.getElementById('conc').style.display = 'block';
  const lev = gL(t);
  /* Гейт функционирования (DSM-5 Критерий D) */
  const _dEg = dS('E');
  const _levIdx = LEV.indexOf(lev);
  let effLev = lev, effIdx = _levIdx, funcGated = false;
  if (_dEg !== null && _dEg / DM.E <= 0.25 && _levIdx > 1) {
    effLev = LEV[1]; effIdx = 1; funcGated = true;
  }
  let h = '';

  /* ДИСКЛЕЙМЕР */
  h += '<div class="ci" style="background:var(--pri-lt);padding:16px;border-radius:10px;margin-bottom:12px;font-size:14px;color:var(--ink2);"><strong style="color:var(--pri);">Важно:</strong> Результат скрининга — не диагноз. Только специалист может поставить диагноз на основании очного осмотра. Этот инструмент основан на критериях DSM-5 и валидированных шкалах, но сам не прошёл независимую клиническую валидацию.</div>';

  /* БЛОК 1: Общий уровень + доменные комментарии */
  if (a >= 3) {
    h += `<div class="ci"><strong>Общий уровень: ${effLev.l} (${t} из ${MAX})</strong>${DESC[effIdx]}.</div>`;
    if (funcGated) {
      h += '<div class="ci" style="background:#fffbeb;padding:16px;border-radius:10px;border-left:4px solid #d97706;"><strong style="color:#d97706;">Функционирование сохранено</strong>Ваш скрининговый балл повышен, но нарушения функционирования не выявлены. Это может указывать на тревожную черту характера, а не на расстройство. Уровень скорректирован. Тем не менее, если симптомы беспокоят — консультация специалиста не помешает.</div>';
    }
    /* Блок «к специалисту» при уровне ≥ лёгкий */
    if (effIdx >= 1) {
      h += '<div class="ci" style="background:var(--sage-lt);padding:16px;border-radius:10px;border-left:4px solid var(--sage);"><strong style="color:var(--sage);">Рекомендуется консультация специалиста</strong>Результат скрининга указывает на признаки, которые стоит обсудить с психиатром, психотерапевтом или клиническим психологом. Это состояние хорошо изучено, и для него существуют эффективные методы помощи при обращении к специалисту.</div>';
    }
    /* Temporal gating: DSM-5 Критерий A (≥6 мес.) */
    if (HIST.x1 === '<3m') {
      h += '<div class="ci" style="background:var(--red-lt);padding:18px;border-radius:10px;border-left:4px solid var(--red);"><strong style="color:var(--red);">Длительность менее 6 месяцев</strong>DSM-5 требует тревоги ≥6 месяцев для ГТР. Ваши симптомы могут быть <strong>адаптационной реакцией на стресс</strong>, а не хроническим расстройством. Это важное отличие — адаптационная реакция часто проходит при разрешении стрессовой ситуации. Если состояние сохранится — пройдите скрининг повторно через 3 месяца.</div>';
    }
    [
      { k:'A', n:'Ядерное беспокойство', mx:DM.A, ad:[
        'Беспокойство в пределах нормы.',
        'Заметное, но управляемое. Техники осознанности, дыхательные практики.',
        'Выраженное неконтролируемое беспокойство. Консультация специалиста поможет разобраться с механизмами тревоги и подобрать эффективный подход.',
        'Тяжёлое. Обратитесь к психиатру или психотерапевту — существуют эффективные методы помощи даже при выраженной тревоге.'
      ]},
      { k:'B', n:'Когнитивный', mx:DM.B, ad:[
        'Когнитивные искажения минимальны.',
        'Заметные: метакогниции о тревоге, мета-тревога. Психообразование о механизмах тревоги.',
        'Выраженные: «тревога полезна», страх от тревоги. Эти когнитивные паттерны хорошо изучены — специалист поможет с ними работать.',
        'Тяжёлые когнитивные паттерны. Обратитесь к специалисту — при выраженных когнитивных искажениях существуют эффективные методы помощи.'
      ]},
      { k:'C', n:'Соматический', mx:DM.C, ad:[
        'Соматические проявления минимальны.',
        'Заметные: напряжение, нарушение сна. Специалист может помочь с техниками управления телесными проявлениями тревоги.',
        'Выраженные соматические проявления. При преобладании телесных симптомов стоит обсудить со специалистом — в том числе для исключения соматических причин.',
        'Тяжёлые соматические проявления. Обратитесь к специалисту — существуют методы помощи при телесных симптомах тревоги.'
      ]},
      { k:'D', n:'Поведенческий', mx:DM.D, ad:[
        'Поведение не изменено.',
        'Заметные: избегание, перепроверки. Специалист поможет постепенно снижать избегание и восстанавливать уверенность.',
        'Выраженные: прокрастинация, поиск заверений, гиперконтроль. Эти поведенческие паттерны поддаются коррекции при обращении к специалисту.',
        'Тяжёлые. Жизнь сузилась. Комплексная помощь.'
      ]}
    ].forEach(d => {
      const s = dS(d.k);
      if (s === null) return;
      const p = s / d.mx;
      const i = p <= .25 ? 0 : p <= .5 ? 1 : p <= .75 ? 2 : 3;
      h += `<div class="ci"><strong>${d.n}: ${s} из ${d.mx} (${Math.round(p * 100)}%)</strong>${d.ad[i]}</div>`;
    });
  }

  /* БЛОК 2: Domain-ratio дифдиагностика */
  if (a >= 5) {
    const pA = dS('A'), pC = dS('C'), pD = dS('D');
    if (pC !== null && pC / DM.C > .6 && pA !== null && pA / DM.A < .3) {
      h += '<div class="ci" style="border-top:2px solid var(--dC);padding-top:18px;margin-top:8px;"><strong style="color:var(--dC);">Высокий соматический при низком беспокойстве</strong>Преобладание телесных симптомов без выраженного когнитивного компонента — необычный паттерн для ГТР. Обсудите это со специалистом — возможны другие причины телесных проявлений.</div>';
    }
    if (pD !== null && pD / DM.D > .6 && pA !== null && pA / DM.A < .3) {
      h += '<div class="ci" style="border-top:2px solid var(--dD);padding-top:18px;margin-top:8px;"><strong style="color:var(--dD);">Высокий поведенческий при низком беспокойстве</strong>Гиперконтроль и перепроверки без выраженной генерализованной тревоги — необычный паттерн для ГТР. Обсудите со специалистом — он поможет разобраться в природе этих проявлений.</div>';
    }
  }

  /* БЛОК 3: Функционирование */
  if (a >= 3) {
    const dE = dS('E');
    if (dE !== null) {
      const p = dE / DM.E;
      h += `<div class="ci"><strong>Функционирование: ${dE} из ${DM.E}</strong>${p <= .15 ? 'Сохранено. Возможна тревожность как черта характера, а не расстройство.' : p <= .5 ? 'Умеренное нарушение. Стоит обсудить со специалистом.' : 'Значительное нарушение. Обратитесь к специалисту — помощь возможна.'}</div>`;
    }
  }

  /* БЛОК 4: История */
  const histKeys = Object.keys(HIST);
  if (histKeys.length > 0) {
    h += '<div class="ci" style="border-top:2px solid var(--brd);padding-top:18px;margin-top:8px;"><strong style="color:var(--pri);">Контекст и история:</strong></div>';
    if (HIST.x1 === '<3m') h += '<div class="ci"><strong>Длительность менее 3 месяцев</strong> — см. предупреждение выше. Если тревога сохраняется ≥6 месяцев — переоцените.</div>';
    if (HIST.x1 === '2y+') h += '<div class="ci"><strong>Более 2 лет.</strong> Хроническое течение. Обратитесь к специалисту — при длительном течении существуют эффективные методы помощи, подобранные индивидуально.</div>';
    if (HIST.x2 === 'regular' || HIST.x2 === 'frequent') h += '<div class="ci" style="background:var(--warm-lt);padding:16px;border-radius:10px;"><strong>Панические атаки.</strong> Это отдельное состояние от ГТР. При ГТР — хроническое беспокойство. При панике — внезапные приступы ужаса. Часто сочетаются (до 30%). Обсудите с психиатром — подход к помощи может отличаться.</div>';
    if (HIST.x3 === 'boredom') h += '<div class="ci" style="background:var(--warm-lt);padding:16px;border-radius:10px;"><strong>Концентрацию нарушает скука, а не тревога.</strong> Это больше характерно для СДВГ. При ГТР — мешают тревожные МЫСЛИ. При СДВГ — мешает СКУКА, а на интересном — гиперфокус. <a href="/screening/adhd/" style="color:var(--pri);font-weight:600;">Пройдите также скрининг СДВГ →</a></div>';
    if (HIST.x3 === 'both') h += '<div class="ci"><strong>И тревога, и скука мешают.</strong> Возможна коморбидность ГТР + СДВГ. До 25% взрослых с СДВГ имеют сопутствующую тревогу. Важно: тревога может быть следствием невыявленного СДВГ — обсудите со специалистом.</div>';
    if (HIST.x4 === 'recent') h += '<div class="ci" style="background:var(--warm-lt);padding:16px;border-radius:10px;"><strong>Тревога связана с конкретной травматической ситуацией.</strong> Если после травмы появились навязчивые воспоминания, кошмары, избегание напоминаний — это может указывать на иной паттерн, чем ГТР. Обсудите со специалистом. <a href="/screening/ptsd/" style="color:var(--pri);font-weight:600;">Пройдите также скрининг ПТСР →</a></div>';
    if (HIST.x4 === 'long_ago') h += '<div class="ci"><strong>Травматическое событие в прошлом.</strong> Давнее событие может влиять на текущую тревогу. Если есть навязчивые воспоминания или избегание — обсудите со специалистом.</div>';
    if (HIST.d1 === 'no') h += '<div class="ci"><strong>Нет ремиссий — тревога постоянный фон.</strong> Хроническое течение. Возможна коморбидность с дистимией (хронической депрессией). Обратитесь к специалисту — при хроническом течении помощь особенно важна.</div>';
    if (HIST.d2 === 'moderate') h += '<div class="ci"><strong>Коморбидная депрессия умеренной или тяжёлой степени.</strong> ГТР + депрессия — частая комбинация (60%). Обратитесь к специалисту — существуют методы, эффективные при обоих состояниях одновременно. <a href="/screening/depression/" style="color:var(--pri);font-weight:600;">Пройдите также скрининг Депрессия →</a></div>';
    if (HIST.d3 === 'yes') h += '<div class="ci"><strong>Тревожные расстройства в семье.</strong> Наследуемость ГТР ~30%. Генетическая предрасположенность подтверждена. Это не «вы такой» — это биология.</div>';
    if (HIST.d4 === 'high') h += '<div class="ci" style="background:var(--warm-lt);padding:16px;border-radius:10px;"><strong>Высокое потребление кофеина (4+ чашки в день).</strong> Кофеин >400 мг/день может значительно усиливать или полностью имитировать симптомы тревожного расстройства: сердцебиение, напряжение, бессонница, беспокойство. Попробуйте снизить потребление и оцените изменения через 2 недели. Если симптомы сохранятся — обсудите со специалистом.</div>';
  }
  document.getElementById('concC').innerHTML = h;
}

